"""
Единый источник истины (Single Source of Truth) для графовой базы данных.
Определяет имена таблиц узлов и строгий перечень возможных связей (ребер) и категорий.
"""

from typing import Literal

# ================================================================
# Схема для общего графа агента
# ================================================================

GRAPH_NODE_TABLE = "Concept"

# Строгий список возможных отношений между концепциями
GRAPH_EDGE_TABLES = [
    "IS_A",  # Наследование / Классификация
    "PART_OF",  # Композиция / Принадлежность
    "REQUIRES",  # Зависимость
    "CAUSES",  # Причина -> Следствие
    "OWNS",  # Обладание / Свойство
    "PRODUCES",  # Результат деятельности
    "CONFLICTS_WITH",  # Антагонизм / Несовместимость
    "RELATES_TO",  # Общая ассоциация (когда остальное не подходит)
]  # Literal для того, чтобы LLM в аргументах понимала, что варианты аргумента строго предопределены

# Тип для строгой валидации связей Pydantic Guard Layer'ом
RelationType = Literal[
    "IS_A", "PART_OF", "REQUIRES", "CAUSES", "OWNS", "PRODUCES", "CONFLICTS_WITH", "RELATES_TO"
]

# Строгий список категорий узлов
ConceptCategory = Literal[
    "PERSON",  # Субъекты: Конкретные люди, пользователи, агенты, авторы
    "ORGANIZATION",  # Группы: Компании, сообщества, команды
    "LOCATION",  # Локации: Физические места, URL-ссылки, пути к файлам
    "SOFTWARE",  # Софт: Программы, скрипты, фреймворки, языки
    "HARDWARE",  # Железо: Физические устройства, ПК, серверы
    "DOCUMENT",  # Данные: Файлы, статьи, логи, репозитории, посты
    "EVENT",  # События: Происшествия, ошибки, встречи, исторические факты
    "PROJECT",  # Процессы: Задачи, глобальные цели, эпики
    "CONCEPT",  # Абстракции: Идеи, алгоритмы, теории, правила поведения
]

# ================================================================
# Схема для Code Graph (AST-граф кодовой базы)
# ================================================================

CODE_NODE_TABLE = "CodeNode"

CODE_EDGE_TABLES = [
    "IMPORTS",  # Файл импортирует файл или класс
    "CONTAINS",  # Файл содержит класс/функцию
    "DEFINES",  # Класс определяет метод
    "CALLS",  # Функция вызывает функцию
]

CodeRelationType = Literal["IMPORTS", "CONTAINS", "DEFINES", "CALLS"]
CodeNodeType = Literal["FILE", "CLASS", "FUNCTION"]
